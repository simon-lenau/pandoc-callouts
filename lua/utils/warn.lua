-- ================================ > warn < ================================ --
-- ┌┌──────────────────────────────────────────────────────────────────────┐┐ --
-- ││ warn                                                                 ││ --
-- └└──────────────────────────────────────────────────────────────────────┘┘ --
local function warn(message)
    io.stderr:write(string.rep("-", 80, ""), "\n", "WARNING: " .. message, "\n", string.rep("-", 80, ""), "\n")
end

-- ───────────────────────────────── <end> ────────────────────────────────── --

return warn
