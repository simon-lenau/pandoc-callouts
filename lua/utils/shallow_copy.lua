-- ============================ > shallow_copy < ============================ --

-- ┌┌──────────────────────────────────────────────────────────────────────┐┐ --
-- ││ create a shallow copy of a table                                     ││ --
-- └└──────────────────────────────────────────────────────────────────────┘┘ --
local function shallow_copy(t)
    local copy = {}
    for k, v in pairs(t) do
        copy[k] = v
    end
    return copy
end

-- ───────────────────────────────── <end> ────────────────────────────────── --

return shallow_copy